const numero1 = 20;
const numero2 = 40;

// Suma
const suma = numero1 + numero2;
console.log('Suma:', suma); // 60

// Resta
const resta = numero1 - numero2;
console.log('Resta:', resta); // -20

// Multiplicación
const multiplicacion = numero1 * numero2;
console.log('Multiplicación:', multiplicacion); // 800

// División
const division = numero1 / numero2;
console.log('División:', division); // 0.5

// Módulo
const modulo = numero2 % numero1;
console.log('Módulo:', modulo); // 0